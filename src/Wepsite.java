
public class Wepsite {

	String Name;
	String Human_number;
	String ID;
	String PW;
	
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getHuman_number() {
		return Human_number;
	}
	public void setHuman_number(String human_number) {
		Human_number = human_number;
	}
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getPW() {
		return PW;
	}
	public void setPW(String pW) {
		PW = pW;
	}
	
}
